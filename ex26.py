# THE OPERATION ON SETS..WE HAVE A UNION AND INTERSECTING.
set1={'ram','akash','deep'}
set2={'guru','deep','vinay'}
set3={'gowtham','puni','vikas','deepu'}
print(set1.union(set2))# WE ARE USING A KEY WORD OF UNION .
print(set1 | set2 | set3)# WE CAN REPRESENT THE UNION IN| SYMBOLE . CAN DO MULTIPLE VALUES COMBINE
# WE CAN UPDATE A SET BY USING A UPDATE KEYWORD.
set1.update(set2)
print(set1)
# WE HAVE A INTERSECTION METHOD .
print(set1.intersection(set2))


